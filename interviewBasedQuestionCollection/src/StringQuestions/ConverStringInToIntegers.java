package StringQuestions;

public class ConverStringInToIntegers {

	public static void main(String[] args) {
		
		String a = "111";
		String b  = "222";
		int a1 = Integer.parseInt(a);
		int b1 = Integer.parseInt(b);
		
		int c1 = a1+b1;
		String c = String.valueOf(c1);
		System.out.println(c);

	}

}
